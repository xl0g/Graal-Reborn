// TileTypeOf returns the tile type for a tile index (0-4095).
// page selects the tile "page": 0 = TYPE0 (interior/mixed), 1 = TYPE1 (overworld).
func TileTypeOf(index int, page int) byte {
	if index < 0 || index >= 4096 {
		return TileBlocking
	}
	if page == 1 {
		return tileType1[index]
	}
	return tileType0[index]
}

// IsSolid reports whether the given tile type blocks movement.
func IsSolid(t byte) bool { return t == TileBlocking }

// IsWater reports whether the tile is deep (impassable) water.
func IsWater(t byte) bool { return t == TileWater }

// IsLava reports whether the tile is lava.
func IsLava(t byte) bool { return t == TileLava || t == TileLavaSwamp }

// IsDamage reports whether the tile hurts the player.
func IsDamage(t byte) bool { return t == TileHurt || t == TileLava || t == TileLavaSwamp }

// IsPassable reports whether the player can walk over this tile.
func IsPassable(t byte) bool {
	return t != TileBlocking && t != TileWater
}

// IsSwamp reports whether the tile is swampy terrain.
func IsSwamp(t byte) bool { return t == TileSwamp || t == TileLavaSwamp }

// IsSit reports whether the tile acts as a seat.
func IsSit(t byte) bool { return t == TileChair }

// IsLie reports whether the tile is a bed.
func IsLie(t byte) bool { return t == TileBedUpper || t == TileBedLower }

